
package com.github.rexfilius.shapescalculatorjava.models;

public class Shape {
    
    private double length;
    private double breadth;
    private double height;
    private double sideLength;
    private double sideLength1;
    private double sideLength2;
    private double topLength;
    private double baseLength;
    private double radius;

    public double getLength() {
        return length;
    }
    public void setLength(double length) {
        this.length = length;
    }
    
    public double getBreadth() {
        return breadth;
    }
    public void setBreadth(double breadth) {
        this.breadth = breadth;
    }
    
    public double getHeight() {
        return height;
    }
    public void setHeight(double height) {
        this.height = height;
    }
    
    public double getSideLength() {
        return sideLength;
    }
    public void setSideLength(double sideLength) {
        this.sideLength = sideLength;
    }
    
    public double getSideLength1() {
        return sideLength1;
    }
    public void setSideLength1(double sideLength1) {
        this.sideLength1 = sideLength1;
    }
    
    public double getSideLength2() {
        return sideLength2;
    }
    public void setSideLength2(double sideLength2) {
        this.sideLength2 = sideLength2;
    }
    
    public double getTopLength() {
        return topLength;
    }
    public void setTopLength(double topLength) {
        this.topLength = topLength;
    }
    
    public double getBaseLength() {
        return baseLength;
    }
    public void setBaseLength(double baseLength) {
        this.baseLength = baseLength;
    }
    
    public double getRadius() {
        return radius;
    }
    public void setRadius(double radius) {
        this.radius = radius;
    }

}
